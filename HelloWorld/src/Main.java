// This is the main class of the program. In Java, every application must contain a class.
public class Main {

    // This is the main method. It is the entry point of any Java application.
    // The 'String[] args' parameter allows the program to accept command-line arguments.
    public static void main(String[] args) {

        // Declare and initialize a String (rerefence type) variable named 'helloWorld' with the value "Hello World".
        String helloWorld = "Hello World"; // This variable holds the message to be printed.

        // Print the value of the 'helloWorld' variable to the console.
        System.out.println(helloWorld);
    }
}
